#include <iostream>
#include <cmath>
using namespace std;

int main() {
    cout << "a) " << abs(-4) << endl;
    cout << "b) " << fabs(10.8) << endl;
    cout << "c) " << fabs(-2.5) << endl;
    cout << "d) " << pow(3.2, 2) << endl;
    cout << "e) " << pow(2.5, 3) << endl;
    cout << "f) " << sqrt(25.0) << endl;
    cout << "g) " << sqrt(6.25) << endl;
    cout << "h) " << pow(3.0, 4.0) / abs(-9) << endl;
    cout << "i) " << floor(28.95) << endl;
    cout << "j) " << ceil(35.2) << endl;
    return 0;
}
